            <h2>1.TOEIC LÀ GÌ?</h2>
            <br />
            <p>
                <b>TOEIC</b> là kỳ thi tiếng Anh quốc tế (viết tắt của cụm từ<b>Test of English for International Communication</b>  – Bài kiểm tra tiếng Anh giao tiếp quốc tế). Bài thi TOEIC kiểm tra trình độ tiếng Anh của một người (người không sử dụng tiếng Anh là ngôn ngữ mẹ đẻ), từ đó xác định được khả năng sử dụng tiếng Anh trong môi trường chuyên nghiệp của người kiểm tra.<br />
                Chứng chỉ TOEIC được các tổ chức giáo dục, doanh nghiệp trên toàn thế giới công nhận. Điểm TOEIC (reading, listening) tối đa 990 (trong đó mỗi kỹ năng chiếm 495 điểm).<br />
                Hiện tại, chứng chỉ TOEIC với mức điểm khoảng 450- 750+ đang là yêu cầu đầu ra của hầu hết các trường đại học/ cao đẳng tại Việt Nam cũng như các doanh nghiệp lấy TOEIC là tiêu chí quan trọng khi tuyển dụng nhân viên.
            </p>

            <br />

