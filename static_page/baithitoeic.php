            <h2>4.BẰNG TOEIC CÓ GIÁ TRỊ NHƯ THẾ NÀO</h2>
            <br />
            <p>Sau khi tham dự bài thi TOEIC, thí sinh sẽ nhận được phiếu điểm báo cáo kết quả với các mục đã được phân tích chi tiết nhằm giúp thí sinh nhận biết được  khả năng của mình trong mỗi phần thi.</p>
            <p>Nếu bạn muốn được cấp thêm bằng chứng chỉ TOEIC (có thể gọi là bằng TOEIC), bạn phải nộp thêm lệ phí để được cấp bằng. Tuy nhiên mọi người đều công nhận phiếu báo điểm này là số điểm TOEIC chính thức. Vì vậy nếu không cần thiết bạn hoàn toàn có thể dùng phiếu báo điểm này.</p>
